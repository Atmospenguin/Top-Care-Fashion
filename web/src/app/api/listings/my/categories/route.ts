import { NextRequest, NextResponse } from "next/server";
import { prisma } from "@/lib/db";
import { createSupabaseServer } from "@/lib/supabase";
import { verifyLegacyToken } from "@/lib/jwt";

/**
 * Ëé∑ÂèñÂΩìÂâçÁôªÂΩïÁî®Êà∑
 */
async function getCurrentUser(req: NextRequest) {
  try {
    const supabase = await createSupabaseServer();

    // ‰ªé Authorization Â§¥ËØªÂèñ token
    const authHeader = req.headers.get("authorization");
    const token = authHeader?.startsWith("Bearer ")
      ? authHeader.split(" ")[1]
      : null;

    let dbUser: any = null;

    if (token) {
      // ‰ºòÂÖàÂ∞ùËØïÊú¨Âú∞ JWTÔºàlegacyÔºâ
      const v = verifyLegacyToken(token);
      if (v.valid && v.payload?.uid) {
        dbUser = await prisma.users.findUnique({ where: { id: Number(v.payload.uid) } });
      }

      // ÂÜçÂ∞ùËØï Supabase JWT
      if (!dbUser) {
        const { data: { user }, error } = await supabase.auth.getUser(token);
        if (user && !error) {
          dbUser = await prisma.users.findUnique({ where: { supabase_user_id: user.id } });
        }
      }
    }

    if (!dbUser) {
      return null;
    }

    return dbUser;
  } catch (err) {
    console.error("‚ùå getCurrentUser failed:", err);
    return null;
  }
}

/**
 * Ëé∑ÂèñÂΩìÂâçÁî®Êà∑listings‰∏≠ÂÆûÈôÖ‰ΩøÁî®ÁöÑÂàÜÁ±ª
 */
export async function GET(req: NextRequest) {
  try {
    const user = await getCurrentUser(req);
    if (!user) {
      return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
    }

    console.log("üìñ Loading categories for user:", user.id);

    // Ëé∑ÂèñÁî®Êà∑listings‰∏≠ÂÆûÈôÖ‰ΩøÁî®ÁöÑÂàÜÁ±ªÔºàÂè™ËÆ°ÁÆóactive listingsÔºâ
    const categories = await prisma.listing_categories.findMany({
      where: {
        listings: {
          some: {
            seller_id: user.id,
            listed: true,
            sold: false,
          },
        },
      },
      select: {
        id: true,
        name: true,
        description: true,
        _count: {
          select: {
            listings: {
              where: {
                seller_id: user.id,
                listed: true,
                sold: false,
              },
            },
          },
        },
      },
      orderBy: {
        name: "asc",
      },
    });

    // Ê†ºÂºèÂåñÂàÜÁ±ªÊï∞ÊçÆ
    const formattedCategories = categories.map((category) => ({
      id: category.id,
      name: category.name,
      description: category.description,
      count: category._count.listings,
    }));

    console.log(`‚úÖ Found ${formattedCategories.length} categories for user ${user.id}`);

    return NextResponse.json({
      success: true,
      categories: formattedCategories,
    });

  } catch (error) {
    console.error("‚ùå Error fetching user categories:", error);
    return NextResponse.json(
      { error: "Failed to fetch categories" },
      { status: 500 }
    );
  }
}
